# operator digunakan untuk menjalankan operasi
# antara kedua variabel atau value
# contoh


angka = 20
angka_kedua = 50

# operator penambahan
print(30 + 20)


# operator pengurangan
print(angka_kedua - angka)


# operator perkalian
print(3 * 5)

# operator pembagi
print(20 * 5)

# operator modulus
# oeprator modulus menghitung nilai sisa bagi
# dari suatu operasi matematika
# sisa hasil bagi adalah bagian dari hasil
# pembagian yang tidak menghasilkan bilangan
# bulat
# contoh
# misalnya 7 / 2 = 3.5
# berarti 3 sisa 1 krna sistem pembagian
# dengan metode porogapit
# contoh pembagian porogapit
# https://id.wikihow.com/Melakukan-Pembagian-Bersusun-Panjang
print(100 % 5)

# operator perpangkatan
print(4**2)

# operator floor division
# mengambil nilai utama dari suatu pembagian
# sebagai contoh
# 2 / 7 = 0.28
# maka floor division hanya mengambil angka
# angka utamanya yaitu 0
print(30 // 2)
