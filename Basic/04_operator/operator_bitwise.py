angka_saya = 30
angka_saya1 = 6

print("operator bitwise")
print("angka_saya & angka =", angka_saya & angka_saya1)
print("angka_saya | angka =", angka_saya | angka_saya1)
print("angka_saya ^ angka =", angka_saya ^ angka_saya1)
print("~angka_saya =", ~angka_saya)


print("\noperator bitwise left and right")
angka_lain = 30
angka_lain1 = -30
print("angka_lain = 30 >> 1 =", angka_lain >> 1)
print("angka_lain1 = -30 >> 1 =", angka_lain1 >> 1)

print("angka lain = 30 << 1 =", angka_lain << 1)
print("angka_lain1 = -30 << 1 =", angka_lain1 << 1)
