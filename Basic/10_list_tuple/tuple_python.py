jenis_mobil = ("ferrari", "bmw", "jaguar", "lamborghini")

# menampilkan isi dari tuple
print(jenis_mobil)

# menampilkan isi dari tuple dengan index
print(jenis_mobil[0])

# menduplikasi item pada list
jenis_mobil_terbaru = ("ferrari", "bmw", "jaguar", "bmw")
print(jenis_mobil_terbaru)

# menghitung panjang tuple
print(len(jenis_mobil))

# membuat satu item dengan tuple
jenis_makanan = ("seblak",)
print(jenis_makanan)

# membuat tipe data tuple
tuple_campur = ("makan", 12, True, 12.0)
print(tuple_campur)

# menampilkan tuple dengan perulangan for
for mobil in jenis_mobil:
    print(mobil)
