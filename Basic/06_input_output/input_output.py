# input digunakan untuk
# memasukkan variabel secara dinamis
# output digunakan untuk
# contoh
nama = "joes"
print(nama)

# mengganti nama 'joes' menjadi sesuai user
nama = input("masukkan nama :")
print(nama)

# menggunakan inputan sesuai tipe data
# jika memasukkan inputan selain angka integer
# akan terjadi error
angka = int(input("masukkan angka :"))
print(angka)
